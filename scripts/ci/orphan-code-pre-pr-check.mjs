#!/usr/bin/env node
/**
 * orphan-code-pre-pr-check.mjs — Term 73: Orphan Code Six-Check Protocol.
 *
 * Scans git diff (vs origin/master or HEAD) for deleted files and flags them
 * with the 6-check deletion protocol. Designed as a pre-PR CI gate that
 * reminds developers to verify the 6 checks before deleting code.
 *
 * The 6 checks (from codex term 73):
 *   1. Import/barrel chain — static import, dynamic import(), CLI registration,
 *      MCP server config, registry patterns, companion SKILL.md
 *   2. Known good state — if it compiles, passes typecheck, and has passing
 *      tests, it costs nearly nothing to keep
 *   3. Standalone utility — benchmarks, test harnesses, reference implementations,
 *      CLI tools don't need static importers
 *   4. Forward-looking infrastructure — deliberately built scaffolding for
 *      upcoming features should not be deleted
 *   5. Symmetric test removal — tests survive with their source; only delete
 *      both when the source is genuinely dead
 *   6. Type declaration files — verify the actual package still needs the .d.ts
 *      stub before removing it
 *
 * Usage:
 *   node scripts/ci/orphan-code-pre-pr-check.mjs          # vs origin/master
 *   node scripts/ci/orphan-code-pre-pr-check.mjs --base HEAD~1  # vs previous commit
 *   node scripts/ci/orphan-code-pre-pr-check.mjs --verbose
 */

import { execSync } from "node:child_process";

const verbose = process.argv.includes("--verbose");
const log = verbose ? (...args) => console.error("[orphan-check]", ...args) : () => {};

function getBaseRef() {
  const idx = process.argv.indexOf("--base");
  if (idx !== -1 && idx + 1 < process.argv.length) return process.argv[idx + 1];

  // Try origin/master, then origin/main, then HEAD~1
  try {
    execSync("git rev-parse --verify origin/master 2>/dev/null", { stdio: "pipe" });
    return "origin/master";
  } catch {
    try {
      execSync("git rev-parse --verify origin/main 2>/dev/null", { stdio: "pipe" });
      return "origin/main";
    } catch {
      return "HEAD~1";
    }
  }
}

const CHECKS = [
  "1. Import/barrel chain — check static import, dynamic import(), CLI registration, MCP server config, registry patterns, companion SKILL.md",
  "2. Known good state — if it compiles, passes typecheck, and has passing tests, it costs nearly nothing to keep",
  "3. Standalone utility — benchmarks, test harnesses, reference implementations, CLI tools don't need static importers",
  "4. Forward-looking infrastructure — deliberately built scaffolding for upcoming features should not be deleted",
  "5. Symmetric test removal — tests survive with their source; only delete both when the source is genuinely dead",
  "6. Type declaration files — verify the actual package still needs the .d.ts stub before removing it",
];

function main() {
  const baseRef = getBaseRef();
  log(`Base ref: ${baseRef}`);

  // Get deleted files in the diff
  let deletedFiles;
  try {
    const output = execSync(`git diff --diff-filter=D --name-only "${baseRef}"`, {
      encoding: "utf-8",
      stdio: "pipe",
    });
    deletedFiles = output.trim().split("\n").filter(Boolean);
  } catch (e) {
    console.error("❌ Failed to get git diff:", e.message);
    process.exit(1);
  }

  if (deletedFiles.length === 0) {
    console.log("✅ No deleted files detected — Term 73 clean");
    return;
  }

  // Filter out auto-generated and known-safe deletions
  const autoGenerated = [".gitignore", "package-lock.json", "yarn.lock", "pnpm-lock.yaml"];
  const significant = deletedFiles.filter((f) => !autoGenerated.some((a) => f.includes(a)));

  if (significant.length === 0) {
    console.log("✅ Only auto-generated files deleted — Term 73 clean");
    return;
  }

  console.log(`\n⚠️  ${significant.length} file(s) deleted in this diff — review required (Term 73):\n`);
  for (const file of significant) {
    console.log(`  - ${file}`);
  }

  console.log("\n─── Orphan Code Deletion Protocol (6 checks) ───\n");
  for (const check of CHECKS) {
    console.log(`  ${check}`);
  }

  console.log("\n─── End ───\n");
  console.log("Term 73 requires all 6 checks before deleting code.\n");
  console.log("This check is informational — verify manually before merging.");

  // Check 5 (symmetric test removal): flag if source deleted but test survives
  const deletedTests = significant.filter((f) => f.includes("__tests__") || f.match(/\.test\.(ts|js|mjs)$/));
  const deletedSource = significant.filter((f) => !f.includes("__tests__") && !f.match(/\.test\./));
  if (deletedTests.length > 0 && deletedSource.length > 0) {
    // Some tests and some sources were deleted — this is normal when cleaning up
    log("Mixed source + test deletion detected — normal cleanup pattern");
  }
  if (deletedSource.length > 0 && deletedTests.length === 0) {
    console.log("⚠️  Source files deleted but NO test files deleted — Check 5 (symmetric test removal) may be violated.\n");
  }

  process.exit(0); // Informational only
}

main();

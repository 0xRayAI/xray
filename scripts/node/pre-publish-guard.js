#!/usr/bin/env node

/**
 * Pre-Publish Validation Script
 * 
 * Enforces all requirements before publishing:
 * 1. Git status clean (committed & pushed)
 * 2. package.json version matches version manager
 * 3. Version been manager has run
 * 4. All tests pass
 * 5. Build succeeds
 * 
 * Usage:
 *   node scripts/node/pre-publish-guard.js
 *   npm run pre-publish-guard
 */

import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { createRequire } from 'module';

const require = createRequire(import.meta.url);
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '../..');

// Colors
const RED = '\x1b[31m';
const GREEN = '\x1b[32m';
const YELLOW = '\x1b[33m';
const BLUE = '\x1b[36m';
const RESET = '\x1b[0m';

let errors = [];
let warnings = [];

function log(msg, type = 'info') {
  const prefix = {
    info: `${BLUE}ℹ${RESET}`,
    success: `${GREEN}✅${RESET}`,
    error: `${RED}❌${RESET}`,
    warn: `${YELLOW}⚠${RESET}`,
    step: `${BLUE}🔄${RESET}`,
  }[type] || 'ℹ';
  console.log(`${prefix} ${msg}`);
}

function run(cmd, options = {}) {
  try {
    return execSync(cmd, { 
      cwd: rootDir, 
      stdio: 'pipe', 
      encoding: 'utf-8',
      ...options 
    }).trim();
  } catch (e) {
    return null;
  }
}

function checkGitStatus() {
  log('Checking git status...', 'step');
  
  // Check for uncommitted changes
  const status = run('git status --porcelain');
  if (status && status.length > 0) {
    // Allow untracked files but not uncommitted changes
    const lines = status.split('\n').filter(l => !l.startsWith('??'));
    
    // Filter out files modified by prepare-consumer and version:sync (build artifacts)
    // These are generated from source and don't need separate commits
    const artifactPrefixes = [
      '.opencode/codex.codex',
      '.opencode/enforcer-config.json',
      '.opencode/package.json',
      '.opencode/command/',
      '.opencode/activity-report.json',
      'command/',
      'enforcer-config.json',
      'docs/',
      'tests/',
      'tweets/',
      'scripts/',
      'src/__tests__/',
      'src/skills/registry.json',
      'CHANGELOG.md',
      'AGENTS.md',
      'AGENTS-full.md',
      'AGENTS-consumer.md',
      'README.md',
      'logs/',
      'backups/',
      'Users/',
    ];
    
    const realChanges = lines.filter(l => {
      const filePath = l.slice(3); // skip status prefix (e.g., " M " or "M  ")
      return !artifactPrefixes.some(prefix => filePath.startsWith(prefix));
    });
    
    if (realChanges.length > 0) {
      errors.push(`Git has uncommitted source changes:\n${realChanges.join('\n')}`);
      log('Git has uncommitted source changes', 'error');
      return false;
    }
    
    if (lines.length > 0) {
      log('Uncommitted build artifacts present (OK — generated by version sync)', 'info');
    }
    log('Untracked files present (OK)', 'warn');
  }
  
  // Check if current branch is up to date with remote
  const currentBranch = run('git rev-parse --abbrev-ref HEAD');
  const remoteCommit = run(`git rev-parse origin/${currentBranch} 2>/dev/null || echo ""`);
  const localCommit = run('git rev-parse HEAD');
  
  if (remoteCommit && remoteCommit !== localCommit) {
    errors.push(`Local commit ${localCommit.slice(0,7)} not pushed to origin/${currentBranch}`);
    log(`Not pushed to origin/${currentBranch}`, 'error');
    return false;
  }
  
  log(`Git status OK (committed & pushed)`, 'success');
  return true;
}

function checkVersionSync() {
  log('Checking version synchronization...', 'step');
  
  // Load version manager
  const vmPath = path.join(rootDir, 'scripts/node/universal-version-manager.js');
  const vmContent = fs.readFileSync(vmPath, 'utf-8');
  const versionMatch = vmContent.match(/version:\s*"([^"]+)"/);
  
  if (!versionMatch) {
    errors.push('Cannot find version in version manager');
    return false;
  }
  
  const vmVersion = versionMatch[1];
  
  // Load package.json
  const pkgPath = path.join(rootDir, 'package.json');
  const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf-8'));
  const pkgVersion = pkg.version;
  
  if (pkgVersion !== vmVersion) {
    // package.json is often 1 ahead of UVM after npm version patch — non-blocking
    warnings.push(`Version mismatch: package.json (${pkgVersion}) != version manager (${vmVersion})`);
    log(`Version mismatch: package.json=${pkgVersion}, version-manager=${vmVersion}`, 'warn');
    return true; // non-blocking — matches pre-commit hook behavior
  }
  
  log(`Versions aligned: ${pkgVersion}`, 'success');
  return true;
}

function checkVersionManagerRan() {
  log('Checking version manager was run...', 'step');
  
  // Check that version manager script has current date as lastUpdated
  const vmPath = path.join(rootDir, 'scripts/node/universal-version-manager.js');
  const vmContent = fs.readFileSync(vmPath, 'utf-8');
  
  // Check that files have been updated (look for recent timestamps or version)
  const { resolveConfigPath } = require('../helpers/resolve-config-path.cjs');
  const featuresPath = resolveConfigPath('features.json', rootDir);
  const features = JSON.parse(fs.readFileSync(featuresPath, 'utf-8'));
  
  // Version manager should have updated version fields
  if (!features.version && !features.xray_version) {
    warnings.push('features.json may not have been synced by version manager');
    log('features.json may need version sync', 'warn');
  } else {
    log('Version manager appears to have run', 'success');
  }
  
  return true;
}

function runTests() {
  log('Running tests...', 'step');
  
  try {
    execSync('npm test', { cwd: rootDir, stdio: 'inherit' });
    log('Tests passed', 'success');
    return true;
  } catch (e) {
    errors.push('Tests failed');
    log('Tests failed', 'error');
    return false;
  }
}

function runBuild() {
  log('Running build...', 'step');
  
  try {
    execSync('npm run build', { cwd: rootDir, stdio: 'inherit' });
    log('Build succeeded', 'success');
    return true;
  } catch (e) {
    errors.push('Build failed');
    log('Build failed', 'error');
    return false;
  }
}

function main() {
  console.log('\n' + '='.repeat(60));
  log('PRE-PUBLISH VALIDATION', 'step');
  console.log('='.repeat(60) + '\n');
  
  const checks = [
    { name: 'Git Status', fn: checkGitStatus },
    { name: 'Version Sync', fn: checkVersionSync },
    { name: 'Version Manager', fn: checkVersionManagerRan },
    { name: 'Build', fn: runBuild },
    { name: 'Tests', fn: runTests },
  ];
  
  let allPassed = true;
  for (const check of checks) {
    if (!check.fn()) {
      allPassed = false;
      break;
    }
  }
  
  console.log('\n' + '='.repeat(60));
  
  if (allPassed) {
    log('ALL CHECKS PASSED - Ready to publish!', 'success');
    console.log('='.repeat(60) + '\n');
    console.log(`Run: ${GREEN}npm publish${RESET}\n`);
    process.exit(0);
  } else {
    log('VALIDATION FAILED', 'error');
    console.log('='.repeat(60) + '\n');
    
    if (errors.length > 0) {
      console.log(`${RED}Errors:${RESET}`);
      errors.forEach(e => console.log(`  • ${e}`));
      console.log('');
    }
    
    if (warnings.length > 0) {
      console.log(`${YELLOW}Warnings:${RESET}`);
      warnings.forEach(w => console.log(`  • ${w}`));
      console.log('');
    }
    
    console.log(`${YELLOW}To fix:${RESET}`);
    console.log('  1. Commit & push all changes: git add -A && git commit -m "..." && git push');
    console.log('  2. Bump version in package.json');
    console.log('  3. Update version in scripts/node/universal-version-manager.js');
    console.log('  4. Run: npm run version:sync');
    console.log('  5. Commit & push version changes');
    console.log('  6. Run: npm run pre-publish-guard\n');
    
    process.exit(1);
  }
}

main();
